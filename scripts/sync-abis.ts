/**
 * Extract ABIs from forge artifacts and write them as typed TS modules into
 * `frontend/lib/abis/`. Uses `as const` so wagmi/viem can infer call signatures.
 *
 * Usage: `pnpm sync:abis`
 *
 * One file is produced per contract. Run after `forge build`. Committed to the repo
 * so Vercel builds don't need Foundry.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(__dirname, "..");
const OUT_DIR = join(ROOT, "contracts/out");
const ABI_DIR = join(ROOT, "frontend/lib/abis");

// Contracts we ship to the frontend. Libraries (LMSRPricing) are only used by
// Market internally, so they are not exported.
const CONTRACTS = ["MockUSDC", "Shares", "Resolution", "MarketFactory", "Market", "Dispenser"] as const;

function loadAbi(contractName: string): unknown[] {
  const artifactPath = join(OUT_DIR, `${contractName}.sol`, `${contractName}.json`);
  if (!existsSync(artifactPath)) {
    throw new Error(`Artifact not found: ${artifactPath}. Did you run 'forge build'?`);
  }
  const artifact = JSON.parse(readFileSync(artifactPath, "utf8"));
  if (!Array.isArray(artifact.abi)) {
    throw new Error(`Invalid artifact (no ABI array): ${artifactPath}`);
  }
  return artifact.abi;
}

function formatAbiModule(name: string, abi: unknown[]): string {
  const header = [
    "// This file is generated by scripts/sync-abis.ts. Do not edit by hand.",
    "// Run `pnpm sync:abis` after changing contracts.",
    "",
  ].join("\n");
  return `${header}export const ${name}Abi = ${JSON.stringify(abi, null, 2)} as const;\n`;
}

function formatIndexModule(names: readonly string[]): string {
  const header = [
    "// This file is generated by scripts/sync-abis.ts. Do not edit by hand.",
    "",
  ].join("\n");
  const reexports = names.map((n) => `export { ${n}Abi } from "./${n}";`).join("\n");
  return `${header}${reexports}\n`;
}

function main() {
  mkdirSync(ABI_DIR, { recursive: true });
  for (const name of CONTRACTS) {
    const abi = loadAbi(name);
    writeFileSync(join(ABI_DIR, `${name}.ts`), formatAbiModule(name, abi));
    console.log(`  ✓ ${name}  (${abi.length} entries)`);
  }
  writeFileSync(join(ABI_DIR, "index.ts"), formatIndexModule(CONTRACTS));
  console.log(`\nWrote ${CONTRACTS.length + 1} files to ${ABI_DIR}`);
}

main();
